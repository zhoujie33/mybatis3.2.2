package com.sz.yb.model;

public class Pages {
    private int start;//当前页
    private int size;//每页显示数量

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
